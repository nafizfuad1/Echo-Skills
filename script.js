//  GLOBAL STATE
let currentIndex = 0;
let currentData = [];
let answers = [];

// ELEMENTS
const home = document.getElementById("home");
const slider = document.getElementById("slider");

const slideImage = document.getElementById("slideImage");
const title = document.getElementById("slider-title");
const description = document.getElementById("description");
const question = document.getElementById("question");
const progress = document.getElementById("progress");

const yesBtn = document.getElementById("yesBtn");
const noBtn = document.getElementById("noBtn");
const prevBtn = document.getElementById("prev");
const nextBtn = document.getElementById("next");

const sliderTypeTitle = document.getElementById("slider-type-title");

// DATA MAP
const allData = {
  frontend,
  backend,
  fullstack,
};
function saveState() {
  const state = {
    currentIndex,
    currentType,
    answers,
  };
  localStorage.setItem("sliderState", JSON.stringify(state));
}

// HOME BUTTON CLICK
document.querySelectorAll(".hero-button").forEach((btn) => {
  btn.addEventListener("click", () => {
    console.log("clicked");
    const type = btn.dataset.type;
    startSlider(type);
  });
});

let currentType = "";

function startSlider(type) {
  currentType = type;
  currentData = skills[type].data;
  currentIndex = 0;
  answers = [];

  sliderTypeTitle.textContent = skills[type].progressTitle;

  home.classList.add("hidden");
  slider.classList.remove("hidden");

  renderSlide();
}

//  RENDER SLIDE
function renderSlide() {
  const slide = currentData[currentIndex];

  slideImage.src = slide.imageURL;
  title.textContent = slide.title;
  description.textContent = slide.description;
  question.textContent = slide.question;

  yesBtn.classList.remove("active");
  noBtn.classList.remove("active");

  if (answers[currentIndex] === "Yes") yesBtn.classList.add("active");
  if (answers[currentIndex] === "No") noBtn.classList.add("active");

  updateProgress();
}
function updateProgress() {
  const percent = (currentIndex / currentData.length) * 100;
  progress.style.width = percent + "%";
}

// YES / NO
yesBtn.addEventListener("click", () => {
  answers[currentIndex] = "Yes";
  saveState();
  nextSlide();
});

noBtn.addEventListener("click", () => {
  answers[currentIndex] = "No";
  saveState();
  nextSlide();
});

//  ARROWS
prevBtn.addEventListener("click", () => {
  if (currentIndex > 0) {
    currentIndex--;
    saveState();
    renderSlide();
  }
});

nextBtn.addEventListener("click", nextSlide);

function nextSlide() {
  if (currentIndex < currentData.length - 1) {
    currentIndex++;
    saveState();
    renderSlide();
  } else {
    showResult();
    saveState();
  }
}

//  RESULT PAGE
function showResult() {
  // Hide slider section
  slider.classList.add("hidden");

  // Show home section (reusing layout)
  home.classList.remove("hidden");

  // Update left section with result content
  const leftSection = document.querySelector(
    ".home-left .left-section-content",
  );
  leftSection.innerHTML = `
    <h2 class="thanks hero-title">Thank you!</h2>
    <p class="result-text hero-text">
      Here is the summary of your ${currentType} development skill:
    </p>

    <ul class="result-list">
      ${currentData
        .map(
          (item, index) =>
            `<li class="icon-item">
              <img src="./img/${answers[index] === "Yes" ? "yes" : "no"}.svg" alt="${
                answers[index]
              }"> 
              ${item.title}
            </li>`,
        )
        .join("")}
    </ul>

   <a class="go-home" onclick="goHome()"> <img class="back-arrow-img" src="./img/back-arrow.png"> <p>Go to home</p> </a>
  `;

  // Keep hero image on the right (or any illustration)
  const rightSection = document.querySelector(".home-right .hero-img");
  rightSection.innerHTML = `
    <img src="./img/result-img.png" alt="hero" class="main-hero-image">
  `;
}

//  GO HOME
function goHome() {
  localStorage.removeItem("sliderState"); // clear saved state
  location.reload();
}

function loadState() {
  const savedState = localStorage.getItem("sliderState");
  if (savedState) {
    const state = JSON.parse(savedState);
    currentIndex = state.currentIndex ?? 0;
    currentType = state.currentType ?? "";
    answers = state.answers ?? [];

    if (currentType && allData[currentType]) {
      currentData = allData[currentType];

      if (currentIndex < currentData.length) {
        home.classList.add("hidden");
        slider.classList.remove("hidden");
        renderSlide();
      } else {
        showResult();
      }
    }
  }
}

loadState();
